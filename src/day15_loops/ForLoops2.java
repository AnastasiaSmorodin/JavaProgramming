package day15_loops;

public class ForLoops2 {
    public static void main(String[] args) {
        for (char i = 65; i <= 90; i++) {
            System.out.print(i + " ");//all upper a-z
        }
        System.out.println();
        System.out.println("___________________________________");
        for (char i = 'a'; i <= 'z'; i++) {
            System.out.print(i + " ");
        }
        System.out.println();
        System.out.println("__________________________");

        for (char i = 'Z';i>='A'; i-- ){
            System.out.print(i+" ");
        }
        System.out.println();
        for(char i ='z';i>='a'; i--){
            System.out.print(i+" ");
        }








    }
}
