package day18Nestedloop;

public class AaBbCc {
    public static void main(String[] args) {
        for (char k = 'A'; k <= 'E'; k++) {
            System.out.println();
            for (char i = 'a'; i <= 'z'; i++) {
                System.out.print("" + k + i + " ");
            }

        }

    }
}
